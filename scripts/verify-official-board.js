// Official board gate for game-interaction-prd-expander.
// Run with Figma use_figma after rendering and validation. Set CONFIG.targetBoardId.

const CONFIG = {
  targetBoardId: "",
  namespace: "game_interaction_prd_expander",
  requiredRendererVersion: "manifest-renderer-v0.9",
  requiredValidatorVersion: "manifest-validator-v1.0",
  requiredRendererScriptHash: "render-brief-board:scaffold-llm-content:2026-06-24",
  requiredValidatorScriptHash: "figma-board-validator:scaffold-llm-content:2026-06-24",
  requiredTemplateNodeId: "1184:1331"
};

const REQUIRED_VALIDATOR_CHECKS = [
  "forbidden-fallback-node-scan",
  "llm-wireframe-slot-scan",
  "llm-note-slot-scan",
  "footer-bounds-scan",
  "ai-pending-visible-scan",
  "flow-count-scan",
  "semantic-note-tag-scan",
  "flow-edge-integrity-scan",
  "semantic-review-required-scan"
];

function parseJson(value) {
  try { return JSON.parse(value); } catch (_error) { return null; }
}

function isContainer(node) {
  return "children" in node;
}

function walk(node, visit) {
  visit(node);
  if (!isContainer(node)) return;
  for (const child of node.children) walk(child, visit);
}

function isActuallyVisible(node) {
  let current = node;
  while (current) {
    if ("visible" in current && current.visible === false) return false;
    current = current.parent;
  }
  return true;
}

function issue(issues, level, code, message, node) {
  issues.push({ level, code, message, nodeId: node ? node.id : undefined, nodeName: node ? node.name : undefined });
}

function validateActualBoardState(board, renderRecord, issues) {
  const forbiddenNames = new Set(["generated missing text slot", "flow screen generated title"]);
  const forbidden = [];
  const featureFrames = [];
  const footerNodes = [];
  walk(board, (node) => {
    if (!isActuallyVisible(node)) return;
    if (forbiddenNames.has(node.name)) forbidden.push(node);
    if (String(node.name || "").startsWith("llm wireframe slot ")) featureFrames.push(node);
    if (node.name === "底部装饰" || node.name === "footer") footerNodes.push(node);
  });
  if (forbidden.length > 0) {
    issue(issues, "fail", "FORBIDDEN_FALLBACK_NODE_PRESENT", `Forbidden fallback nodes are visible (${forbidden.length}).`, forbidden[0]);
  }
  const expected = Number((renderRecord && (renderRecord.expectedWireframeCount || renderRecord.expectedFeatureInterfaceCount)) || 0);
  if (expected > 0 && featureFrames.length < expected) {
    issue(issues, "fail", "WIREFRAME_SLOT_COUNT_MISMATCH", `Expected ${expected} LLM wireframe slots, found ${featureFrames.length}.`, board);
  }
  for (const frame of featureFrames) {
    let visibleNodes = 0;
    let textNodes = 0;
    let placeholder = null;
    walk(frame, (node) => {
      if (!isActuallyVisible(node)) return;
      if (node !== frame) visibleNodes += 1;
      if (node.type === "TEXT") textNodes += 1;
      if (node.type === "TEXT" && /^(原型|Prototype)$/i.test(String(node.characters || "").trim())) placeholder = node;
    });
    if (placeholder) issue(issues, "fail", "WIREFRAME_PLACEHOLDER_REMAINS", "Visible feature frame still shows prototype placeholder.", placeholder);
    if (visibleNodes < 8) issue(issues, "fail", "WIREFRAME_NOT_RENDERED", `Feature frame has too little LLM-drawn interface content (${visibleNodes}).`, frame);
    if (textNodes > 0 && textNodes >= visibleNodes - 2) issue(issues, "fail", "WIREFRAME_TEXT_ONLY", "Feature frame looks text-only instead of a player-visible wireframe.", frame);
  }
  for (const footer of footerNodes) {
    if ("y" in footer && "height" in footer) {
      const gap = board.height - (footer.y + footer.height);
      if (gap < -4 || gap > 80) issue(issues, "fail", "FOOTER_NOT_AT_BOTTOM", `Footer should sit at board bottom; gap=${gap}.`, footer);
    }
  }
}

async function main() {
  if (!CONFIG.targetBoardId) throw new Error("CONFIG.targetBoardId is required.");
  const issues = [];
  const board = await figma.getNodeByIdAsync(CONFIG.targetBoardId);
  if (!board || !isContainer(board)) throw new Error(`Target board not found or not inspectable: ${CONFIG.targetBoardId}`);

  const renderRecord = parseJson(board.getSharedPluginData(CONFIG.namespace, "renderRecord"));
  const manifest = parseJson(board.getSharedPluginData(CONFIG.namespace, "manifest"));
  const validation = parseJson(board.getSharedPluginData(CONFIG.namespace, "lastValidation"));
  const localRecord = parseJson(board.getSharedPluginData(CONFIG.namespace, "codexLocalRecord"));
  if (localRecord) {
    issue(issues, "fail", "CODEX_LOCAL_RECORD_PRESENT", "Board contains codexLocalRecord fallback metadata. Fallback boards cannot pass the official gate.", board);
  }

  if (!renderRecord) {
    issue(issues, "fail", "RENDER_RECORD_MISSING", "No official renderRecord was found. The board was not generated by the manifest renderer.", board);
  } else if (renderRecord.rendererVersion !== CONFIG.requiredRendererVersion) {
    issue(issues, "fail", "UNOFFICIAL_RENDERER", `Expected ${CONFIG.requiredRendererVersion}, found ${renderRecord.rendererVersion || "unknown"}.`, board);
  }
  if (renderRecord && renderRecord.scriptHash !== CONFIG.requiredRendererScriptHash) {
    issue(issues, "fail", "RENDERER_SCRIPT_HASH_MISMATCH", `Expected renderer scriptHash ${CONFIG.requiredRendererScriptHash}, found ${renderRecord.scriptHash || "missing"}.`, board);
  }
  if (renderRecord && renderRecord.semanticReviewRequired !== true) {
    issue(issues, "fail", "SEMANTIC_REVIEW_NOT_REQUIRED", "Official output must require LLM semantic review after structural validation.", board);
  }

  if (!manifest) {
    issue(issues, "fail", "MANIFEST_SHARED_DATA_MISSING", "Official renderer must write manifest shared data on the board.", board);
  } else if (!manifest.source_template || manifest.source_template.node_id !== CONFIG.requiredTemplateNodeId) {
    issue(issues, "fail", "MANIFEST_TEMPLATE_MISMATCH", `Expected template node ${CONFIG.requiredTemplateNodeId}.`, board);
  }

  if (!validation) {
    issue(issues, "fail", "VALIDATION_RECORD_MISSING", "No official validator record was found.", board);
  } else {
    if (validation.validatorVersion !== CONFIG.requiredValidatorVersion) {
      issue(issues, "fail", "UNOFFICIAL_VALIDATOR", `Expected ${CONFIG.requiredValidatorVersion}, found ${validation.validatorVersion || "unknown"}.`, board);
    }
    if (validation.scriptHash !== CONFIG.requiredValidatorScriptHash) {
      issue(issues, "fail", "VALIDATOR_SCRIPT_HASH_MISMATCH", `Expected validator scriptHash ${CONFIG.requiredValidatorScriptHash}, found ${validation.scriptHash || "missing"}.`, board);
    }
    const checks = new Set(Array.isArray(validation.checks) ? validation.checks : []);
    const missingChecks = REQUIRED_VALIDATOR_CHECKS.filter((check) => !checks.has(check));
    if (missingChecks.length > 0) {
      issue(issues, "fail", "VALIDATOR_CHECKS_MISSING", `Validator record is missing required checks: ${missingChecks.join(", ")}.`, board);
    }
    if (validation.passed !== true) {
      issue(issues, "fail", "VALIDATOR_NOT_PASSED", "Official validator did not pass.", board);
    }
  }

  const templateNames = ["顶部悬浮", "时间记录", "流程名", "主标题", "副标题", "界面标题", "部门标注", "底部装饰"];
  const counts = Object.fromEntries(templateNames.map((name) => [name, 0]));
  walk(board, (node) => {
    if (Object.prototype.hasOwnProperty.call(counts, node.name)) counts[node.name] += 1;
  });
  const missingTemplateNames = templateNames.filter((name) => counts[name] === 0);
  if (missingTemplateNames.length > 0) {
    issue(issues, "fail", "TEMPLATE_ANCHORS_MISSING", `Missing cloned template anchors: ${missingTemplateNames.join(", ")}. This usually means the board was hand-drawn or generated by a template-inspired renderer.`, board);
  }

  validateActualBoardState(board, renderRecord, issues);

  const failCount = issues.filter((item) => item.level === "fail").length;
  const warnCount = issues.filter((item) => item.level === "warn").length;
  const report = {
    gateVersion: "official-board-gate-v0.6",
    passed: failCount === 0,
    failCount,
    warnCount,
    boardId: board.id,
    boardName: board.name,
    rendererVersion: renderRecord && renderRecord.rendererVersion,
    validatorVersion: validation && validation.validatorVersion,
    templateAnchorCounts: counts,
    issues
  };
  board.setSharedPluginData(CONFIG.namespace, "officialGate", JSON.stringify(report));
  return report;
}

return await main();
