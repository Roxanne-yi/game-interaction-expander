function assertConfig() {
  getManifestConfig();
  getAnalysisConfig();
}

function decodeBase64Utf8(value) {
  if (!value) return "";
  const binary = atob(String(value).replace(/\s+/g, ""));
  const bytes = [];
  for (let i = 0; i < binary.length; i += 1) bytes.push(binary.charCodeAt(i));
  if (typeof TextDecoder !== "undefined") return new TextDecoder("utf-8").decode(new Uint8Array(bytes));
  return decodeURIComponent(bytes.map((byte) => `%${byte.toString(16).padStart(2, "0")}`).join(""));
}

function getConfigJson(kind) {
  const base64Key = `${kind}Base64`;
  const rawValue = CONFIG[base64Key]
    ? decodeBase64Utf8(CONFIG[base64Key])
    : CONFIG[kind];
  const placeholder = kind === "manifest" ? "__TEMPLATE_MANIFEST_JSON__" : "__ANALYSIS_JSON__";
  if (!rawValue || rawValue === placeholder) {
    throw new Error(`CONFIG.${kind} is not set. Use ${base64Key} for Chinese-safe payload transport, or paste the JSON object directly into CONFIG.${kind}.`);
  }
  const parsed = typeof rawValue === "string" ? JSON.parse(rawValue) : rawValue;
  assertNoDamagedText(parsed, `CONFIG.${kind}`);
  return parsed;
}

function getManifestConfig() {
  return getConfigJson("manifest");
}

function getAnalysisConfig() {
  const analysis = getConfigJson("analysis");
  if (analysis.schemaVersion !== REQUIRED_ANALYSIS_SCHEMA_VERSION) {
    throw new Error(`CONFIG.analysis must be generated by scripts/build-render-payload.js (${REQUIRED_ANALYSIS_SCHEMA_VERSION}). Received: ${analysis.schemaVersion || "missing"}.`);
  }
  if (!analysis.modelSummary || typeof analysis.modelSummary !== "object") {
    throw new Error("CONFIG.analysis is missing modelSummary. Run scripts/build-render-payload.js instead of hand-assembling the renderer payload.");
  }
  return analysis;
}

function assertNoDamagedText(value, path) {
  if (typeof value === "string") {
    if (/\?{3,}|\uFFFD{2,}/.test(value)) {
      throw new Error(`${path} contains replacement/question-mark text. Stop and rebuild the payload with UTF-8/base64 transport; do not render fallback boards.`);
    }
    return;
  }
  if (Array.isArray(value)) {
    value.forEach((item, index) => assertNoDamagedText(item, `${path}[${index}]`));
    return;
  }
  if (value && typeof value === "object") {
    for (const [key, item] of Object.entries(value)) assertNoDamagedText(item, `${path}.${key}`);
  }
}

function isContainer(node) {
  return "children" in node;
}

function walk(node, visit) {
  visit(node);
  if (!isContainer(node)) return;
  for (const child of node.children) walk(child, visit);
}

function approx(a, b, tolerance = CONFIG.tolerance) {
  return Math.abs(Number(a || 0) - Number(b || 0)) <= tolerance;
}

function getBounds(node) {
  if (node && node.absoluteBoundingBox) return node.absoluteBoundingBox;
  return null;
}

function relativeBounds(node, root) {
  const nb = getBounds(node);
  const rb = getBounds(root);
  if (!nb || !rb) return null;
  return {
    x: nb.x - rb.x,
    y: nb.y - rb.y,
    width: nb.width,
    height: nb.height
  };
}

function rightmostX(page) {
  let max = 0;
  for (const child of page.children) {
    if ("x" in child && "width" in child) max = Math.max(max, child.x + child.width);
  }
  return max;
}

async function setPageIfNeeded() {
  if (!CONFIG.targetPageId) return;
  const page = await figma.getNodeByIdAsync(CONFIG.targetPageId);
  if (!page || page.type !== "PAGE") throw new Error(`targetPageId is not a page: ${CONFIG.targetPageId}`);
  await figma.setCurrentPageAsync(page);
}

async function instantiateTemplate(manifest, analysis) {
  const template = await figma.getNodeByIdAsync(manifest.source_template.node_id);
  if (!template) throw new Error(`Template node not found: ${manifest.source_template.node_id}`);
  if (!("width" in template) || !("height" in template)) throw new Error("Template node is not frame-like.");

  let board;
  let createdFrom = template.type;
  if (template.type === "COMPONENT" && typeof template.createInstance === "function") {
    board = template.createInstance();
  } else if ("clone" in template) {
    board = template.clone();
  } else {
    throw new Error("Template node cannot be cloned or instantiated.");
  }

  figma.currentPage.appendChild(board);
  board.name = analysis.boardName || "Interaction pre-brief v0.1";
  board.x = rightmostX(figma.currentPage) + CONFIG.gapFromRightmostFrame;
  board.y = 0;

  if (board.type === "INSTANCE" && typeof board.detachInstance === "function") {
    board = board.detachInstance();
    board.name = analysis.boardName || "Interaction pre-brief v0.1";
  }

  return { board, template, createdFrom };
}

async function getBoard(manifest, analysis) {
  if (!CONFIG.targetBoardId) return instantiateTemplate(manifest, analysis);
  const board = await figma.getNodeByIdAsync(CONFIG.targetBoardId);
  if (!board || !("children" in board)) throw new Error(`targetBoardId is not a board-like node: ${CONFIG.targetBoardId}`);
  const template = await figma.getNodeByIdAsync(manifest.source_template.node_id);
  if (!template) throw new Error(`Template node not found: ${manifest.source_template.node_id}`);
  return { board, template, createdFrom: "existing-board" };
}

function parseSharedJson(owner, namespace, key, fallback = null) {
  if (!owner || typeof owner.getSharedPluginData !== "function") return fallback;
  const raw = owner.getSharedPluginData(namespace, key);
  if (!raw) return fallback;
  try {
    return JSON.parse(raw);
  } catch (error) {
    throw new Error(`Invalid shared data JSON for ${namespace}/${key}: ${error.message}`);
  }
}

function uniqueStages(stages) {
  return [...new Set((Array.isArray(stages) ? stages : []).filter(Boolean))];
}

function writeRenderSession(board, manifest, session) {
  const normalized = {
    ...session,
    boardId: board.id,
    boardName: board.name,
    templateNodeId: manifest.source_template.node_id,
    stagesCompleted: uniqueStages(session.stagesCompleted)
  };
  const text = JSON.stringify(normalized);
  figma.currentPage.setSharedPluginData(manifest.namespace, RENDER_SESSION_KEY, text);
  board.setSharedPluginData(manifest.namespace, RENDER_SESSION_KEY, text);
  return normalized;
}

async function readRenderSession(manifest) {
  const session = parseSharedJson(figma.currentPage, manifest.namespace, RENDER_SESSION_KEY, null);
  if (!session || !session.boardId) {
    throw new Error("renderer_transport_failed: missing renderSession. Run figma-render.00-clone-template.generated.js before section render stages.");
  }
  if (session.templateNodeId && session.templateNodeId !== manifest.source_template.node_id) {
    throw new Error(`renderer_transport_failed: renderSession template mismatch. Expected ${manifest.source_template.node_id}, received ${session.templateNodeId}.`);
  }
  return session;
}

async function getStagedBoard(manifest) {
  const session = await readRenderSession(manifest);
  const board = await figma.getNodeByIdAsync(session.boardId);
  if (!board || !("children" in board)) throw new Error(`renderer_transport_failed: staged board not found: ${session.boardId}`);
  const template = await figma.getNodeByIdAsync(manifest.source_template.node_id);
  if (!template) throw new Error(`Template node not found: ${manifest.source_template.node_id}`);
  return { board, template, session };
}

function markStageCompleted(board, manifest, session, stage) {
  return writeRenderSession(board, manifest, {
    ...session,
    stagesCompleted: uniqueStages([...(session.stagesCompleted || []), stage])
  });
}

async function sourceRel(sourceRoot, sourceId) {
  const source = await figma.getNodeByIdAsync(sourceId);
  if (!source) throw new Error(`Manifest source node not found: ${sourceId}`);
  const rel = relativeBounds(source, sourceRoot);
  if (!rel) throw new Error(`Manifest source node has no bounds: ${sourceId}`);
  return { source, rel };
}

async function resolveFromSourceId(board, sourceRoot, sourceId, options = {}) {
  const { source, rel } = await sourceRel(sourceRoot, sourceId);
  const candidates = [];
  walk(board, (node) => {
    if (!("width" in node) || !("height" in node)) return;
    if (options.type && node.type !== options.type) return;
    if (!options.type && node.type !== source.type) return;
    const rb = relativeBounds(node, board);
    if (!rb) return;
    if (!approx(rb.x, rel.x) || !approx(rb.y, rel.y) || !approx(rb.width, rel.width) || !approx(rb.height, rel.height)) return;
    if (options.name !== false && source.name && node.name !== source.name) return;
    candidates.push(node);
  });
  if (candidates.length === 0) throw new Error(`Could not resolve manifest slot in cloned board: ${sourceId} (${source.name})`);
  return candidates[0];
}

async function loadTextFonts(textNode) {
  if (textNode.type !== "TEXT") return;
  const fonts = new Map();
  const segments = textNode.getStyledTextSegments(["fontName"]);
  for (const seg of segments) {
    if (seg.fontName && seg.fontName !== figma.mixed) fonts.set(`${seg.fontName.family}::${seg.fontName.style}`, seg.fontName);
  }
  for (const font of fonts.values()) await figma.loadFontAsync(font);
}

async function replaceText(textNode, value, options = {}) {
  if (!textNode || textNode.type !== "TEXT") throw new Error("replaceText target is not a TEXT node.");
  if (value === undefined || value === null || (value === "" && !options.allowEmpty)) return false;
  await loadTextFonts(textNode);
  textNode.characters = String(value);
  if (options.name) textNode.name = options.name;
  return true;
}

async function replaceSlotText(board, template, sourceId, value, options = {}) {
  const node = await resolveFromSourceId(board, template, sourceId, { type: "TEXT" });
  await replaceText(node, value, options);
  return node.id;
}

async function snapshotProtected(board, template, ids) {
  const snapshots = [];
  for (const sourceId of ids) {
    const node = await resolveFromSourceId(board, template, sourceId, { name: false });
    snapshots.push({
      id: node.id,
      sourceId,
      name: node.name,
      type: node.type,
      rel: relativeBounds(node, board),
      text: node.type === "TEXT" ? node.characters : undefined
    });
  }
  return snapshots;
}

async function fillHeader(board, template, manifest, analysis) {
  const slots = manifest.slots.header.text_slots;
  const header = analysis.header || {};
  await replaceSlotText(board, template, slots.project_title.node, header.projectTitle);
  await replaceSlotText(board, template, slots.planner_owner.node, header.planner);
  await replaceSlotText(board, template, slots.ued_owner.node, header.ued);
  await replaceSlotText(board, template, slots.date.node, header.date || analysis.generatedDate);
}

async function fillOverview(board, template, manifest, analysis) {
  const overview = analysis.overview || {};
  const slots = manifest.slots.overview.body_slots;
  await replaceSlotText(board, template, slots.problem.body, overview.problem);
  await replaceSlotText(board, template, slots.target_audience.body, overview.targetAudience);
  await replaceSlotText(board, template, slots.expected_effect.body, overview.expectedEffect);
  await normalizeVersionTable(board, template, manifest, analysis);
}

async function normalizeVersionTable(board, template, manifest, analysis) {
  const table = manifest.slots.overview.version_table;
  const rowNodes = [];
  for (const rowId of table.row_prototypes) {
    rowNodes.push(await resolveFromSourceId(board, template, rowId, { name: false }));
  }
  rowNodes.sort((a, b) => a.y - b.y);

  const rowData = (analysis.overview && analysis.overview.versionRow) || {};
  const mode = analysis.mode || "create";
  if (mode === "create") {
    for (let i = 1; i < rowNodes.length; i += 1) setVisibleDeep(rowNodes[i], false);
    await fillVersionRow(rowNodes[0], rowData);
  } else {
    const newRow = rowNodes[rowNodes.length - 1].clone();
    rowNodes[0].parent.appendChild(newRow);
    newRow.y = rowNodes[rowNodes.length - 1].y + rowNodes[rowNodes.length - 1].height;
    await fillVersionRow(newRow, rowData);
  }
}

async function fillVersionRow(row, rowData) {
  const texts = [];
  walk(row, (node) => {
    if (node.type === "TEXT") texts.push(node);
  });
  texts.sort((a, b) => a.x - b.x);
  await replaceText(texts[0], rowData.version || "v0.1");
  await replaceText(texts[1], rowData.change || "根据 PRD 生成交互预解析");
  await replaceText(texts[2], rowData.date || new Date().toISOString().slice(0, 10));
}

function setVisible(node, visible) {
  if ("visible" in node) node.visible = visible;
}

function setVisibleDeep(node, visible) {
  walk(node, (child) => setVisible(child, visible));
}

function directChildrenInRange(board, top, bottom, excludeIds = new Set()) {
  return board.children.filter((node) => {
    if (!("y" in node) || !("height" in node)) return false;
    if (excludeIds.has(node.id)) return false;
    return node.y >= top && node.y < bottom;
  });
}

function bottomOf(nodes) {
  if (!nodes.length) return 0;
  return Math.max(...nodes.filter((node) => "y" in node && "height" in node).map((node) => node.y + node.height));
}

function topOf(nodes) {
  if (!nodes.length) return 0;
  return Math.min(...nodes.filter((node) => "y" in node).map((node) => node.y));
}

function shiftNodes(nodes, deltaY) {
  if (!deltaY) return;
  for (const node of nodes) {
    if ("y" in node) node.y += deltaY;
  }
}

async function fillFlows(board, template, manifest, analysis) {
  const flows = Array.isArray(analysis.flows) ? analysis.flows : [];
  if (flows.length === 0) return;
  const flowManifest = manifest.slots.flow;
  const sourceChip = await resolveFromSourceId(board, template, flowManifest.flow_name_chip, { name: false });
  const sourceDiagram = await resolveFromSourceId(board, template, flowManifest.diagram_group, { name: false });

  let previousBottom = sourceDiagram.y + sourceDiagram.height;
  for (let i = 0; i < flows.length; i += 1) {
    const flow = flows[i];
    let chip = sourceChip;
    let diagram = sourceDiagram;
    if (i > 0) {
      chip = sourceChip.clone();
      diagram = sourceDiagram.clone();
      board.appendChild(chip);
      board.appendChild(diagram);
      chip.y = previousBottom + 160;
      diagram.y = chip.y + 83;
      previousBottom = diagram.y + diagram.height;
    }
    await replaceTextInSubtree(chip, flow.name || (i === 0 ? "主流程" : `次流程 ${i}`), /流程名称|流程名/);
    await fillFlowDiagram(diagram, flowManifest, flow);
  }
}

async function replaceTextInSubtree(root, value, pattern) {
  let target = null;
  walk(root, (node) => {
    if (target || node.type !== "TEXT") return;
    if (!pattern || pattern.test(node.characters || "") || pattern.test(node.name || "")) target = node;
  });
  if (!target) throw new Error(`No text target found under ${root.name}`);
  await replaceText(target, value);
}

async function fillFlowDiagram(diagram, flowManifest, flow) {
  const steps = Array.isArray(flow.steps) ? flow.steps : [];
  const nodeGroups = [];
  walk(diagram, (node) => {
    if (node.type === "GROUP" && /Group\s+\d+/.test(node.name || "")) {
      const hasPlaceholder = node.children && node.children.some((child) => "width" in child && child.width >= 500 && child.height >= 250);
      if (hasPlaceholder) nodeGroups.push(node);
    }
  });
  nodeGroups.sort((a, b) => a.y - b.y || a.x - b.x);
  if (nodeGroups.length === 0) throw new Error(`Flow "${flow.name}" has no template screen node prototype.`);
  const prototypeNode = nodeGroups[0];
  const neededGroups = nodeGroups.slice();
  for (let i = nodeGroups.length; i < steps.length; i += 1) {
    const cloned = prototypeNode.clone();
    appendNear(prototypeNode, cloned);
    neededGroups.push(cloned);
  }
  const firstRel = relativeBounds(prototypeNode, diagram) || { x: prototypeNode.x, y: prototypeNode.y };
  for (let i = 0; i < neededGroups.length; i += 1) {
    if (i >= steps.length) {
      setVisibleDeep(neededGroups[i], false);
      continue;
    }
    setVisibleDeep(neededGroups[i], true);
    const col = i % CONFIG.flowMaxColumns;
    const row = Math.floor(i / CONFIG.flowMaxColumns);
    neededGroups[i].x = firstRel.x + col * (CONFIG.flowNodeWidth + CONFIG.flowNodeGapX);
    neededGroups[i].y = firstRel.y + row * (CONFIG.flowNodeHeight + CONFIG.flowNodeGapY);
    neededGroups[i].name = "flow screen node";
    await fillFlowScreenNode(neededGroups[i], steps[i].screenName || steps[i].title || `界面 ${i + 1}`);
  }

  const labelTexts = [];
  const connectorNodes = [];
  walk(diagram, (node) => {
    if (node.type === "TEXT" && (/触发条件/.test(node.characters || "") || /condition/i.test(node.name || ""))) labelTexts.push(node);
    if (node.type === "VECTOR" && /Arrow|箭头/i.test(node.name || "")) connectorNodes.push(node);
  });
  for (const connector of connectorNodes) setVisible(connector, false);
  for (const label of labelTexts) setVisible(label, false);
  await renderFlowEdges(diagram, neededGroups.slice(0, steps.length), steps, normalizeFlowEdges(flow, steps), connectorNodes[0], labelTexts[0]);
}

async function fillFlowScreenNode(root, screenName) {
  const texts = [];
  walk(root, (node) => {
    if (node.type === "TEXT") texts.push(node);
  });
  const target = texts.find((node) => /界面名称|screen|name/i.test(node.characters || "") || /界面名称|screen|name/i.test(node.name || "")) || texts[0];
  if (!target) throw new Error(`Flow screen node "${root.name}" has no interface-name text slot.`);
  await replaceText(target, screenName);
  for (const node of texts) {
    if (node !== target) setVisible(node, false);
  }
}

function normalizeFlowEdges(flow, steps) {
  if (Array.isArray(flow.edges) && flow.edges.length > 0) return flow.edges;
  const result = [];
  for (let index = 0; index < steps.length; index += 1) {
    const step = steps[index] || {};
    const directTargets = [step.next, step.nextId, step.to, step.target].filter((value) => value !== undefined && value !== null && value !== "");
    for (const target of directTargets) {
      result.push({ from: index, to: target, label: step.condition || step.label || "" });
    }
    const branches = Array.isArray(step.branches) ? step.branches : [];
    for (const branch of branches) {
      if (!branch) continue;
      const target = branch.to ?? branch.target ?? branch.next ?? branch.nextId ?? branch.screenName;
      if (target === undefined || target === null || target === "") continue;
      result.push({ from: index, to: target, label: branch.label || branch.condition || branch.name || "" });
    }
  }
  if (result.length > 0) return result;
  return steps.slice(1).map((_step, index) => ({ from: index, to: index + 1, label: steps[index + 1].condition || "" }));
}

async function renderFlowEdges(diagram, nodeGroups, steps, edges, connectorPrototype, labelPrototype) {
  const normalizedEdges = Array.isArray(edges) ? edges : [];
  if (steps.length > 1 && normalizedEdges.length === 0) throw new Error(`Flow "${diagram.name}" has no edges. Provide flow.edges, step.next, or step.branches; do not render template-implied player paths.`);
  for (const edge of normalizedEdges) {
    const fromIndex = resolveFlowIndex(edge.from, steps);
    const toIndex = resolveFlowIndex(edge.to, steps);
    if (fromIndex < 0 || toIndex < 0 || !nodeGroups[fromIndex] || !nodeGroups[toIndex]) {
      throw new Error(`Flow edge references missing step: ${JSON.stringify(edge)}`);
    }
    const from = nodeGroups[fromIndex];
    const to = nodeGroups[toIndex];
    const start = centerPoint(from);
    const end = centerPoint(to);
    const connector = connectorPrototype ? connectorPrototype.clone() : figma.createLine();
    appendNear(from, connector);
    connector.name = "flow edge connector";
    setVisible(connector, true);
    styleFlowConnector(connector, start, end);
    const labelText = edge.label || edge.condition || "";
    if (labelText) {
      const label = labelPrototype ? labelPrototype.clone() : await createFlowLabel();
      appendNear(from, label);
      label.name = "flow edge label";
      setVisible(label, true);
      label.x = (start.x + end.x) / 2 - label.width / 2;
      label.y = (start.y + end.y) / 2 - label.height - 10;
      await replaceText(label, labelText);
    }
  }
}

function resolveFlowIndex(value, steps) {
  if (typeof value === "number") return value;
  if (typeof value === "string") {
    const numeric = Number(value);
    if (Number.isInteger(numeric)) return numeric;
    return steps.findIndex((step) => step.id === value || step.screenName === value);
  }
  return -1;
}

function centerPoint(node) {
  return { x: node.x + node.width / 2, y: node.y + node.height / 2 };
}

function styleFlowConnector(connector, start, end) {
  const dx = end.x - start.x;
  const dy = end.y - start.y;
  const length = Math.max(1, Math.sqrt(dx * dx + dy * dy));
  connector.x = start.x;
  connector.y = start.y;
  if (typeof connector.resize === "function") connector.resize(length, Math.max(1, connector.height || 1));
  connector.rotation = Math.atan2(dy, dx) * 180 / Math.PI;
  if ("strokes" in connector) connector.strokes = [{ type: "SOLID", color: { r: 0.16, g: 0.56, b: 0.95 } }];
  if ("strokeWeight" in connector) connector.strokeWeight = 4;
  try {
    if ("strokeCap" in connector) connector.strokeCap = "ARROW_LINES";
  } catch (_error) {
    // Some cloned vectors do not support strokeCap.
  }
}

async function createFlowLabel() {
  const label = figma.createText();
  await figma.loadFontAsync({ family: "Inter", style: "Regular" });
  label.fontName = { family: "Inter", style: "Regular" };
  label.fontSize = 18;
  label.fills = [{ type: "SOLID", color: { r: 0.2, g: 0.2, b: 0.2 } }];
  return label;
}

function appendNear(reference, node) {
  const parent = reference && reference.parent && typeof reference.parent.appendChild === "function" ? reference.parent : figma.currentPage;
  parent.appendChild(node);
}

async function fillFeatureModules(board, template, manifest, analysis) {
  const modules = Array.isArray(analysis.featureModules) ? analysis.featureModules : [];
  if (modules.length === 0) return;
  const detail = manifest.slots.feature_detail;
  const sourceStart = await resolveFromSourceId(board, template, detail.module_block.start, { name: false });
  const redStart = await resolveFromSourceId(board, template, manifest.slots.red_dot.major_title.container, { name: false });
  const footer = await resolveFromSourceId(board, template, manifest.slots.footer.node, { name: false });
  const blockNodes = directChildrenInRange(board, sourceStart.y, redStart.y);
  const redDotNodes = directChildrenInRange(board, redStart.y, footer ? footer.y : Number.POSITIVE_INFINITY, new Set([footer && footer.id].filter(Boolean)));
  const blockTop = Math.min(...blockNodes.map((node) => node.y));
  const blockBottom = Math.max(...blockNodes.map((node) => node.y + node.height));
  const blockHeight = blockBottom - blockTop;
  const moduleGap = Number(manifest.slots.feature_detail.module_block.module_gap || 180);
  const blocks = [blockNodes];

  // Clone every extra module from the untouched template block before filling text.
  // Otherwise later clones inherit the first module's filled labels and lose slot anchors.
  for (let i = 1; i < modules.length; i += 1) {
    const clonedNodes = blockNodes.map((node) => {
        const cloned = node.clone();
        board.appendChild(cloned);
        cloned.y = node.y + i * (blockHeight + moduleGap);
        return cloned;
    });
    blocks.push(clonedNodes);
  }

  let accumulatedExtraHeight = 0;
  for (let i = 0; i < modules.length; i += 1) {
    if (accumulatedExtraHeight) shiftNodes(blocks[i], accumulatedExtraHeight);
    const beforeBottom = bottomOf(blocks[i]);
    const result = await fillModuleBlock(blocks[i], modules[i], i + 1);
    blocks[i] = result.nodes;
    const afterBottom = result.bottom;
    accumulatedExtraHeight += Math.max(0, afterBottom - beforeBottom);
  }

  const generatedBottom = bottomOf(blocks.flat());
  if (analysis.redDot) {
    const redTop = topOf(redDotNodes);
    shiftNodes(redDotNodes, Math.max(0, generatedBottom + moduleGap - redTop));
  } else {
    for (const node of redDotNodes) setVisibleDeep(node, false);
  }
}

async function ensureFeatureModulePrototype(board, template, manifest, session) {
  if (Array.isArray(session.featurePrototypeNodeIds) && session.featurePrototypeNodeIds.length > 0) {
    const prototypeNodes = [];
    for (const id of session.featurePrototypeNodeIds) {
      const node = await figma.getNodeByIdAsync(id);
      if (node) prototypeNodes.push(node);
    }
    if (prototypeNodes.length === session.featurePrototypeNodeIds.length) return session;
  }

  const detail = manifest.slots.feature_detail;
  const sourceStart = await resolveFromSourceId(board, template, detail.module_block.start, { name: false });
  const redStart = await resolveFromSourceId(board, template, manifest.slots.red_dot.major_title.container, { name: false });
  const footer = await resolveFromSourceId(board, template, manifest.slots.footer.node, { name: false });
  const blockNodes = directChildrenInRange(board, sourceStart.y, redStart.y);
  if (blockNodes.length === 0) throw new Error("Missing 4.0 feature module block prototype.");
  const blockTop = topOf(blockNodes);
  const blockBottom = bottomOf(blockNodes);
  const prototypeNodes = blockNodes.map((node) => {
    const cloned = node.clone();
    board.appendChild(cloned);
    cloned.name = `official hidden feature module prototype / ${node.name || node.type}`;
    setVisibleDeep(cloned, false);
    return cloned;
  });
  return writeRenderSession(board, manifest, {
    ...session,
    featureTemplateTop: blockTop,
    featureTemplateBottom: blockBottom,
    featureBlockHeight: blockBottom - blockTop,
    featureModuleGap: Number(manifest.slots.feature_detail.module_block.module_gap || 180),
    featurePrototypeNodeIds: prototypeNodes.map((node) => node.id),
    featureOriginalBlockNodeIds: blockNodes.map((node) => node.id),
    featureRedStartY: redStart.y,
    featureFooterId: footer ? footer.id : "",
    nextFeatureModuleTop: blockTop
  });
}

async function fillFeatureModuleStage(board, template, manifest, analysis, session, moduleIndex) {
  const modules = Array.isArray(analysis.featureModules) ? analysis.featureModules : [];
  if (modules.length === 0) return markStageCompleted(board, manifest, session, `feature-module:${moduleIndex}`);
  if (moduleIndex < 0 || moduleIndex >= modules.length) throw new Error(`Feature module index out of range: ${moduleIndex + 1}/${modules.length}`);

  let updatedSession = await ensureFeatureModulePrototype(board, template, manifest, session);
  const moduleGap = Number(updatedSession.featureModuleGap || manifest.slots.feature_detail.module_block.module_gap || 180);
  const templateTop = Number(updatedSession.featureTemplateTop || 0);
  const desiredTop = moduleIndex === 0
    ? templateTop
    : Number(updatedSession.nextFeatureModuleTop || (templateTop + moduleIndex * ((updatedSession.featureBlockHeight || 1) + moduleGap)));
  let nodes;
  if (moduleIndex === 0) {
    nodes = [];
    for (const id of updatedSession.featureOriginalBlockNodeIds || []) {
      const node = await figma.getNodeByIdAsync(id);
      if (node) nodes.push(node);
    }
    if (nodes.length === 0) {
      const detail = manifest.slots.feature_detail;
      const sourceStart = await resolveFromSourceId(board, template, detail.module_block.start, { name: false });
      const redStart = await resolveFromSourceId(board, template, manifest.slots.red_dot.major_title.container, { name: false });
      nodes = directChildrenInRange(board, sourceStart.y, redStart.y);
    }
  } else {
    const prototypeNodes = [];
    for (const id of updatedSession.featurePrototypeNodeIds || []) {
      const node = await figma.getNodeByIdAsync(id);
      if (node) prototypeNodes.push(node);
    }
    if (prototypeNodes.length === 0) throw new Error("Missing hidden 4.0 feature module prototype for staged render.");
    nodes = prototypeNodes.map((node) => {
      const cloned = node.clone();
      board.appendChild(cloned);
      setVisibleDeep(cloned, true);
      if ("y" in cloned) cloned.y = node.y + (desiredTop - templateTop);
      return cloned;
    });
  }
  setVisibleDeepForNodes(nodes, true);
  const beforeBottom = bottomOf(nodes);
  const result = await fillModuleBlock(nodes, modules[moduleIndex], moduleIndex + 1);
  const afterBottom = Math.max(result.bottom, beforeBottom);
  updatedSession = writeRenderSession(board, manifest, {
    ...updatedSession,
    nextFeatureModuleTop: afterBottom + moduleGap,
    renderedFeatureModuleCount: Math.max(Number(updatedSession.renderedFeatureModuleCount || 0), moduleIndex + 1)
  });
  return markStageCompleted(board, manifest, updatedSession, `feature-module:${moduleIndex}`);
}

function setVisibleDeepForNodes(nodes, visible) {
  for (const node of nodes) setVisibleDeep(node, visible);
}

async function finalizeFeatureModulesStage(board, template, manifest, analysis, session) {
  const modules = Array.isArray(analysis.featureModules) ? analysis.featureModules : [];
  let updatedSession = session;
  if (modules.length > 0) {
    updatedSession = await ensureFeatureModulePrototype(board, template, manifest, session);
    const redStart = await resolveFromSourceId(board, template, manifest.slots.red_dot.major_title.container, { name: false });
    const footer = await resolveFromSourceId(board, template, manifest.slots.footer.node, { name: false });
    const redDotNodes = directChildrenInRange(board, redStart.y, footer ? footer.y : Number.POSITIVE_INFINITY, new Set([footer && footer.id].filter(Boolean)));
    const generatedBottom = Math.max(Number(updatedSession.nextFeatureModuleTop || 0), maxChildBottom(board, footer && footer.id));
    if (analysis.redDot) {
      const redTop = topOf(redDotNodes);
      shiftNodes(redDotNodes, Math.max(0, generatedBottom - redTop));
    } else {
      for (const node of redDotNodes) setVisibleDeep(node, false);
    }
  }
  await moveFooter(board, template, manifest);
  assertNoForbiddenFallbackNodes(board);
  return markStageCompleted(board, manifest, updatedSession, "finalize-feature-modules");
}

async function fillModuleBlock(nodes, module, index) {
  const allNodes = nodes.slice();
  const rowItems = flattenModuleInterfaces(module);
  if (rowItems.length === 0) throw new Error(`Module ${index} has no interface explanations. Each major function needs at least one interface.`);

  const subtitleContainers = findRootNodesContainingText(allNodes, /^副标题$/)
    .filter((node) => "y" in node)
    .sort((a, b) => a.y - b.y);
  const firstSubtitleContainer = subtitleContainers[0];
  if (!firstSubtitleContainer || !("y" in firstSubtitleContainer)) throw new Error(`Module ${index} missing subtitle prototype.`);
  const rowTop = firstSubtitleContainer.y;
  const rowEnd = subtitleContainers[1] && subtitleContainers[1].y > rowTop ? subtitleContainers[1].y : bottomOf(allNodes);
  const majorNodes = allNodes.filter((node) => !("y" in node) || node.y < rowTop);
  const rowPrototype = allNodes.filter((node) => "y" in node && node.y >= rowTop && node.y < rowEnd);
  const unusedTemplateNodes = allNodes.filter((node) => "y" in node && node.y >= rowEnd);
  if (rowPrototype.length === 0) throw new Error(`Module ${index} missing interface-row prototype.`);
  for (const node of unusedTemplateNodes) setVisibleDeep(node, false);

  await replaceFirstInNodes(majorNodes, /^主标题$/, module.title || `功能模块 ${index}`);
  await replaceFirstInNodes(majorNodes, /备注内容（非必须）/, module.note || "", { allowEmpty: true });

  const rowHeight = Math.max(1, bottomOf(rowPrototype) - topOf(rowPrototype));
  const renderedRows = [];
  for (let rowIndex = 0; rowIndex < rowItems.length; rowIndex += 1) {
    const rowNodes = rowIndex === 0 ? rowPrototype : cloneNodeSet(rowPrototype, rowIndex * (rowHeight + CONFIG.featureInterfaceGap));
    allNodes.push(...(rowIndex === 0 ? [] : rowNodes));
    await fillModuleInterfaceRow(rowNodes, rowItems[rowIndex], module, index, rowIndex + 1);
    renderedRows.push(rowNodes);
  }

  hideTemplatePlaceholders(allNodes);
  return { nodes: allNodes, bottom: bottomOf(allNodes) };
}

function flattenModuleInterfaces(module) {
  const rows = [];
  const subsections = Array.isArray(module.subsections) ? module.subsections : [];
  for (const subsection of subsections) {
    const interfaces = Array.isArray(subsection.interfaces) ? subsection.interfaces : [];
    for (const iface of interfaces) rows.push({ subsection, interface: iface || {} });
  }
  if (rows.length === 0 && module.interface) rows.push({ subsection: { title: "主要操作" }, interface: module.interface });
  return rows;
}

async function fillModuleInterfaceRow(rowNodes, row, module, moduleIndex, rowIndex) {
  const subsection = row.subsection || {};
  const iface = row.interface || {};
  await replaceFirstInNodes(rowNodes, /^副标题$/, subsection.title || `功能点 ${rowIndex}`);
  await replaceFirstInNodes(rowNodes, /备注内容（非必须）/, subsection.note || "", { allowEmpty: true });
  await replaceFirstInNodes(rowNodes, /^界面标题$|^产品定位$/, iface.title || iface.screenName || `界面说明 ${rowIndex}`);
  const screenFrame = markFeatureScreenFrame(rowNodes);
  await renderWireframeIntoFrame(screenFrame, iface, subsection, module, moduleIndex, rowIndex);
  await fillRightNotes(rowNodes, normalizeRightNotes(iface, subsection, module, moduleIndex));
}

function markFeatureScreenFrame(rowNodes) {
  const frames = [];
  walkCollection(rowNodes, (node) => {
    if (!("width" in node) || !("height" in node)) return;
    if (node.width >= 1200 && node.width <= 1400 && node.height >= 700 && node.height <= 800) frames.push(node);
  });
  frames.sort((a, b) => a.y - b.y || a.x - b.x);
  if (!frames[0]) throw new Error("Missing 4.0 left screen frame prototype.");
  frames[0].name = "feature interface screen frame";
  return frames[0];
}

