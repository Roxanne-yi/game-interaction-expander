function createWireRect(parent, name, x, y, width, height, fillHex, strokeHex, opacity = 1) {
  const rect = figma.createRectangle();
  rect.name = name;
  rect.x = x;
  rect.y = y;
  rect.resize(Math.max(1, width), Math.max(1, height));
  rect.fills = [{ type: "SOLID", color: hexToRgb(fillHex), opacity }];
  rect.strokes = [{ type: "SOLID", color: hexToRgb(strokeHex || fillHex), opacity: Math.min(1, opacity + 0.2) }];
  rect.strokeWeight = 1;
  parent.appendChild(rect);
  return rect;
}

async function createWireText(parent, text, x, y, width, height, fontSize = 12, fillHex = "#333333") {
  await ensureInterRegular();
  const node = figma.createText();
  node.name = "wireframe text";
  node.x = x;
  node.y = y;
  node.resize(Math.max(1, width), Math.max(1, height));
  node.fontName = { family: "Inter", style: "Regular" };
  node.fontSize = fontSize;
  node.lineHeight = { unit: "AUTO" };
  node.fills = [{ type: "SOLID", color: hexToRgb(fillHex) }];
  node.characters = truncateText(text, Math.max(8, Math.floor(width / Math.max(7, fontSize * 0.58))));
  parent.appendChild(node);
  return node;
}

function truncateText(text, maxChars) {
  const value = String(text || "").replace(/\s+/g, " ").trim();
  if (value.length <= maxChars) return value;
  return `${value.slice(0, Math.max(1, maxChars - 3))}...`;
}

function findTextNode(nodes, pattern) {
  let found = null;
  walkCollection(nodes, (node) => {
    if (found || node.type !== "TEXT") return;
    if (pattern.test(node.characters || "") || pattern.test(node.name || "")) found = node;
  });
  return found;
}

function findRootNodeContainingText(nodes, pattern) {
  return findRootNodesContainingText(nodes, pattern)[0] || null;
}

function findRootNodesContainingText(nodes, pattern) {
  return nodes.filter((root) => {
    let matched = false;
    walk(root, (node) => {
      if (matched || node.type !== "TEXT") return;
      if (pattern.test(node.characters || "") || pattern.test(node.name || "")) matched = true;
    });
    return matched;
  });
}

async function replaceFirstInNodes(nodes, pattern, value, options = {}) {
  const target = findTextNode(nodes, pattern);
  if (!target) throw new Error(`Missing text slot for ${pattern}`);
  await replaceText(target, value, options);
}

function cloneNodeSet(nodes, deltaY) {
  return nodes.map((node) => {
    const cloned = node.clone();
    appendNear(node, cloned);
    if ("y" in cloned) cloned.y = node.y + deltaY;
    return cloned;
  });
}

function hideTemplatePlaceholders(nodes) {
  walkCollection(nodes, (node) => {
    if (node.type !== "TEXT") return;
    const text = String(node.characters || "").trim();
    if (/^主标题$|^副标题$|^界面标题$|^界面名称$|备注内容（非必须）/.test(text)) {
      setVisible(node, false);
    }
  });
}

function walkCollection(nodes, visit) {
  for (const node of nodes) walk(node, visit);
}

async function fillRightNotes(nodes, notes) {
  const textNodes = [];
  walkCollection(nodes, (node) => {
    if (node.type === "TEXT" && isVisibleInOwnTree(node)) textNodes.push(node);
  });
  textNodes.sort((a, b) => a.y - b.y || a.x - b.x);
  const noteBodies = textNodes
    .filter((node) => /分点|重点|说明|备注内容/.test(node.name || "") || /分点|重点|说明|备注内容/.test(node.characters || ""))
    .sort((a, b) => a.y - b.y || a.x - b.x);
  const labelTexts = textNodes
    .filter((node) => /注意|标注|AI|部门/.test(node.name || "") || /注意|AI|待确认|程序|策划|动效|UI/.test(node.characters || ""))
    .sort((a, b) => a.y - b.y || a.x - b.x);
  if (notes.length === 0) throw new Error("Right-side notes are required for every 4.0 interface.");
  const visibleCount = Math.min(notes.length, noteBodies.length, CONFIG.maxVisibleRightNotesPerInterface);
  if (visibleCount === 0 || labelTexts.length < visibleCount || noteBodies.length < visibleCount) {
    throw new Error(`Right-side note template has insufficient visible label/body slots: notes=${notes.length}, labels=${labelTexts.length}, bodies=${noteBodies.length}.`);
  }
  for (let i = 0; i < notes.length && i < Math.max(noteBodies.length, 1); i += 1) {
    const note = notes[i];
    if (labelTexts[i]) {
      await replaceText(labelTexts[i], note.tag);
      if (isAiPendingTag(note.tag)) applyWarningRed(labelTexts[i]);
    }
    if (noteBodies[i]) await replaceText(noteBodies[i], formatNumberedLines(note.lines || []));
  }
  for (let i = visibleCount; i < labelTexts.length; i += 1) setVisible(labelTexts[i], false);
  for (let i = visibleCount; i < noteBodies.length; i += 1) setVisible(noteBodies[i], false);
}

function isVisibleInOwnTree(node) {
  let current = node;
  while (current) {
    if ("visible" in current && current.visible === false) return false;
    current = current.parent;
  }
  return true;
}

function normalizeRightNotes(firstInterface, firstSub, module, moduleIndex) {
  const rawNotes = Array.isArray(firstInterface.notes) ? firstInterface.notes.slice() : [];
  const notes = rawNotes
    .map(normalizeRawRightNote)
    .filter((note) => note && Array.isArray(note.lines) && note.lines.length > 0);

  const pendingLines = collectPendingLines(firstInterface, firstSub, module);
  const hasAiPending = notes.some((note) => isAiPendingTag(note.tag));
  if (pendingLines.length > 0 && !hasAiPending) notes.push({ tag: "AI待确认", lines: pendingLines });

  if (!notes.some((note) => isAiPendingTag(note.tag))) {
    throw new Error(`Module ${moduleIndex} must include at least one AI待确认 note. Re-read the source and add missing logic/design-risk questions to the analysis payload.`);
  }
  if (notes.some((note) => !note.tag)) {
    throw new Error(`Module ${moduleIndex} has a right-side note without a semantic tag.`);
  }

  if (notes.length > CONFIG.maxVisibleRightNotesPerInterface) {
    const aiIndex = notes.findIndex((note) => isAiPendingTag(note.tag));
    if (aiIndex >= CONFIG.maxVisibleRightNotesPerInterface) {
      const [aiNote] = notes.splice(aiIndex, 1);
      notes.splice(CONFIG.maxVisibleRightNotesPerInterface - 1, 0, aiNote);
    }
  }
  return notes.slice(0, CONFIG.maxVisibleRightNotesPerInterface);
}

function normalizeRawRightNote(note) {
  if (typeof note === "string") {
    const lines = splitNoteLines(note);
    return { tag: normalizeNoteTag("", lines), lines };
  }
  if (!note || typeof note !== "object") return null;
  const lines = [];
  if (Array.isArray(note.lines)) lines.push(...note.lines);
  for (const key of ["text", "body", "description", "question", "content"]) {
    if (typeof note[key] === "string") lines.push(...splitNoteLines(note[key]));
  }
  const normalizedLines = lines.map((line) => String(line || "").trim()).filter(Boolean);
  return {
    tag: normalizeNoteTag(note.tag || note.label || note.type || "", normalizedLines),
    lines: normalizedLines
  };
}

function splitNoteLines(value) {
  return String(value || "")
    .split(/\n|；|;/)
    .map((line) => line.replace(/^\s*\d+[.、]\s*/, "").trim())
    .filter(Boolean);
}

function collectPendingLines(...scopes) {
  const lines = [];
  for (const scope of scopes) {
    if (!scope) continue;
    for (const key of ["aiPending", "aiPendingConfirmations", "pendingConfirmations", "questions", "designRisks"]) {
      const value = scope[key];
      if (!Array.isArray(value)) continue;
      for (const item of value) {
        if (typeof item === "string" && item.trim()) lines.push(item.trim());
        if (item && typeof item === "object" && typeof item.text === "string" && item.text.trim()) lines.push(item.text.trim());
        if (item && typeof item === "object" && typeof item.question === "string" && item.question.trim()) lines.push(item.question.trim());
      }
    }
  }
  return [...new Set(lines)].slice(0, 4);
}

function normalizeNoteTag(tag, lines = []) {
  const value = String(tag || "").trim();
  const scope = `${value} ${lines.join(" ")}`;
  if (/AI|待确认|待补充|不明确|缺少|缺失|未知|需确认|需要确认|待策划确认|待技术确认|question|todo/i.test(scope)) return "AI待确认";
  if (/服务端|客户端|程序|接口|协议|数据|配置|同步|刷新|校验|上限|性能|存储|埋点|日志|异常|状态机/.test(scope)) return "程序注意";
  if (/动效|动画|toast|反馈|音效|震动|特效|过渡|loading|加载/.test(scope)) return "动效注意";
  if (/UI|界面|布局|按钮|弹窗|面板|入口|置灰|显示|隐藏|排序|红点|图标|文案|字体|颜色/.test(scope)) return "UI注意";
  if (/策划|规则|玩法|条件|奖励|消耗|次数|概率|价格|上架|出售|回收|交易/.test(scope)) return "策划注意";
  return value || "策划注意";
}

function isAiPendingTag(tag) {
  return /AI待确认/.test(String(tag || ""));
}

function formatNumberedLines(lines) {
  const source = lines.length > 0 ? lines : ["待补充说明"];
  return source.map((line, index) => /^\d+[.、]/.test(line) ? line : `${index + 1}. ${line}`).join("\n");
}

function applyWarningRed(textNode) {
  const red = CONFIG.manifest.style_tokens.warning_red.fill;
  const rgb = hexToRgb(red);
  textNode.fills = [{ type: "SOLID", color: { r: 1, g: 1, b: 1 } }];
  const tagRoot = textNode.parent || textNode;
  walk(tagRoot, (node) => {
    if (!("fills" in node) || !Array.isArray(node.fills) || node.fills === figma.mixed) return;
    if (node.type === "TEXT" || node.fills.length === 0) return;
    node.fills = [{ type: "SOLID", color: rgb }];
  });
}

function hexToRgb(hex) {
  const normalized = hex.replace("#", "");
  return {
    r: parseInt(normalized.slice(0, 2), 16) / 255,
    g: parseInt(normalized.slice(2, 4), 16) / 255,
    b: parseInt(normalized.slice(4, 6), 16) / 255
  };
}

async function moveFooter(board, template, manifest) {
  const footer = await resolveFromSourceId(board, template, manifest.slots.footer.node, { name: false });
  const bottom = maxChildBottom(board, footer.id);
  const desiredY = Math.max(footer.y, bottom + 180);
  const delta = desiredY - footer.y;
  if (delta > CONFIG.tolerance) {
    footer.y += delta;
    board.resize(board.width, footer.y + footer.height);
  }
}

function maxChildBottom(board, ignoredId) {
  let bottom = 0;
  for (const child of board.children) {
    if (child.id === ignoredId) continue;
    if ("visible" in child && child.visible === false) continue;
    if ("y" in child && "height" in child) bottom = Math.max(bottom, child.y + child.height);
  }
  return bottom;
}

function assertNoForbiddenFallbackNodes(board) {
  const found = [];
  walk(board, (node) => {
    if (FORBIDDEN_FALLBACK_NODE_NAMES.includes(node.name)) found.push(node.name);
  });
  if (found.length > 0) {
    throw new Error(`Forbidden fallback nodes detected after render: ${[...new Set(found)].join(", ")}. Fix manifest slots/prototypes instead of creating fallback text.`);
  }
}

async function main() {
  assertConfig();
  await setPageIfNeeded();
  const manifest = getManifestConfig();
  const analysis = getAnalysisConfig();
  const stage = String(CONFIG.renderStage || "all");
  if (stage !== "all") return runRenderStage(stage, manifest, analysis);

  const { board, template, createdFrom } = await getBoard(manifest, analysis);

  const protectedIds = [
    manifest.slots.header.bar_frame,
    manifest.slots.header.background,
    ...manifest.slots.header.brand_assets,
    ...manifest.slots.adaptation.frames,
    manifest.slots.footer.node
  ];
  const protectedSnapshots = await snapshotProtected(board, template, protectedIds);

  await fillHeader(board, template, manifest, analysis);
  await fillOverview(board, template, manifest, analysis);
  await fillFlows(board, template, manifest, analysis);
  await fillFeatureModules(board, template, manifest, analysis);
  await moveFooter(board, template, manifest);
  assertNoForbiddenFallbackNodes(board);

  const renderRecord = createRenderRecord(board, manifest, analysis, createdFrom, protectedSnapshots, ["all"]);
  board.setSharedPluginData(manifest.namespace, "renderRecord", JSON.stringify(renderRecord));
  board.setSharedPluginData(manifest.namespace, "manifest", JSON.stringify(manifest));
  figma.viewport.scrollAndZoomIntoView([board]);
  return { status: "pass", boardId: board.id, boardName: board.name, rendererVersion: renderRecord.rendererVersion };
}

async function runRenderStage(stage, manifest, analysis) {
  if (stage === "clone-template") return runCloneTemplateStage(manifest, analysis);
  const { board, template, session } = await getStagedBoard(manifest);
  if (stage === "overview") return runOverviewStage(board, template, manifest, analysis, session);
  if (stage === "flows") return runFlowsStage(board, template, manifest, analysis, session);
  if (stage === "feature-module") {
    const options = CONFIG.renderStageOptions || {};
    return runFeatureModuleRenderStage(board, template, manifest, analysis, session, Number(options.moduleIndex || 0));
  }
  if (stage === "finalize") return runFinalizeStage(board, template, manifest, analysis, session);
  throw new Error(`Unknown renderStage: ${stage}`);
}

