function createRenderRecord(board, manifest, analysis, createdFrom, protectedSnapshots, stagesCompleted) {
  return {
    rendererVersion: RENDERER_VERSION,
    scriptHash: RENDERER_SCRIPT_HASH,
    createdAt: new Date().toISOString(),
    manifestSchemaVersion: manifest.schema_version,
    sourceTemplateNodeId: manifest.source_template.node_id,
    boardId: board.id,
    boardName: board.name,
    createdFrom,
    mode: analysis.mode || "create",
    renderMode: "official",
    renderStagesCompleted: uniqueStages(stagesCompleted),
    expectedFeatureInterfaceCount: countFeatureInterfaces(analysis),
    expectedFlowStepCount: countFlowSteps(analysis),
    expectedFlowEdgeCount: countFlowEdges(analysis),
    expectedWireframeCount: countFeatureInterfaces(analysis),
    modelSummary: analysis.modelSummary || null,
    expectedSourceCoverageCount: analysis.modelSummary ? Number(analysis.modelSummary.sourceCoverageCount || 0) : (Array.isArray(analysis.sourceCoverage) ? analysis.sourceCoverage.length : 0),
    protectedSnapshots
  };
}

function countFeatureInterfaces(analysis) {
  const modules = Array.isArray(analysis.featureModules) ? analysis.featureModules : [];
  return modules.reduce((total, module) => total + flattenModuleInterfaces(module).length, 0);
}

function countFlowSteps(analysis) {
  const flows = Array.isArray(analysis.flows) ? analysis.flows : [];
  return flows.reduce((total, flow) => total + (Array.isArray(flow.steps) ? flow.steps.length : 0), 0);
}

function countFlowEdges(analysis) {
  const flows = Array.isArray(analysis.flows) ? analysis.flows : [];
  return flows.reduce((total, flow) => {
    const steps = Array.isArray(flow.steps) ? flow.steps : [];
    return total + normalizeFlowEdges(flow, steps).length;
  }, 0);
}

return await main();
