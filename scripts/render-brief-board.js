async function drawModal(frame, modal, width, height) {
  const title = typeof modal === "string" ? modal : (modal.title || "Confirm");
  const body = typeof modal === "string" ? "" : (modal.body || modal.text || "");
  createWireRect(frame, "wireframe modal mask", 0, 64, width, height - 64, "#000000", "#000000", 0.12);
  const mw = Math.min(520, width - 180);
  const mh = 220;
  const mx = width / 2 - mw / 2;
  const my = height / 2 - mh / 2;
  createWireRect(frame, "wireframe modal panel", mx, my, mw, mh, "#FFFFFF", "#CFCFCF");
  await createWireText(frame, title, mx + 28, my + 28, mw - 56, 24, 18);
  await createWireText(frame, body || "Decision and consequence summary", mx + 28, my + 72, mw - 56, 38, 12);
  createWireRect(frame, "wireframe modal primary", mx + mw - 168, my + mh - 58, 132, 36, "#2F3542", "#2F3542");
  await createWireText(frame, "Confirm", mx + mw - 140, my + mh - 47, 84, 14, 12, "#FFFFFF");
}

