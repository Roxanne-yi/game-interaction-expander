async function renderWireframeIntoFrame(screenFrame, iface, subsection, module, moduleIndex, rowIndex) {
  if (!screenFrame || !("children" in screenFrame)) throw new Error(`Module ${moduleIndex} row ${rowIndex} missing drawable screen frame.`);
  const spec = normalizeWireframeSpec(iface, subsection, module, moduleIndex, rowIndex);
  if (!spec) throw new Error(`Module ${moduleIndex} row ${rowIndex} missing wireframe schema.`);
  for (const child of screenFrame.children.slice()) setVisibleDeep(child, false);
  screenFrame.name = "feature interface screen frame";
  await drawWireframeSpec(screenFrame, spec);
}

function normalizeWireframeSpec(iface, subsection, module, moduleIndex, rowIndex) {
  const wireframe = iface.wireframe || {};
  const description = String(wireframe.description || iface.description || iface.title || "").trim();
  const title = String(wireframe.title || wireframe.screenTitle || iface.title || iface.screenName || subsection.title || `Interface ${rowIndex}`).trim();
  const surface = String(wireframe.surface || wireframe.surfaceType || "panel").trim();
  const regions = Array.isArray(wireframe.regions) ? wireframe.regions : [];
  const controls = Array.isArray(wireframe.controls) ? wireframe.controls : [];
  const states = Array.isArray(wireframe.states) ? wireframe.states : [];
  const feedback = Array.isArray(wireframe.feedback) ? wireframe.feedback : [];
  const modal = wireframe.modal || iface.modal || null;
  const toast = wireframe.toast || iface.toast || null;
  if (!title && !description && regions.length === 0 && controls.length === 0 && states.length === 0) return null;
  return {
    title,
    surface,
    surfaceType: String(wireframe.surfaceType || wireframe.surface || surface || "").trim(),
    layoutIntent: String(wireframe.layoutIntent || wireframe.surfaceType || "").trim(),
    primaryTask: String(wireframe.primaryTask || iface.primaryTask || iface.title || "").trim(),
    description,
    regions: regions.length > 0 ? regions : buildFallbackRegions(title, description, controls, states),
    controls,
    states,
    feedback,
    modal,
    toast,
    primaryAction: wireframe.primaryAction || iface.primaryAction || "",
    secondaryActions: Array.isArray(wireframe.secondaryActions) ? wireframe.secondaryActions : []
  };
}

function buildFallbackRegions(title, description, controls, states) {
  const hints = extractShortPhrases(description).slice(0, 6);
  return [
    { id: "nav", type: "navigation", title: "Navigation", items: [title].filter(Boolean) },
    { id: "content", type: "list", title: title || "Main content", items: hints.length ? hints : ["Primary list/card area", "Selected state", "Empty or disabled state"] },
    { id: "detail", type: "detail", title: "Detail and feedback", items: controls.map((item) => controlLabel(item)).concat(states.map((item) => stateLabel(item))).filter(Boolean).slice(0, 5) }
  ];
}

function extractShortPhrases(text) {
  return String(text || "")
    .split(/[.;。；\n,，]/)
    .map((item) => item.trim())
    .filter(Boolean)
    .map((item) => item.length > 32 ? `${item.slice(0, 31)}...` : item);
}

async function drawWireframeSpec(frame, spec) {
  await ensureInterRegular();
  const w = frame.width;
  const h = frame.height;
  const layout = chooseWireframeLayout(spec);
  createWireRect(frame, "wireframe background", 0, 0, w, h, "#F8F8F8", "#D9D9D9");
  createWireRect(frame, "wireframe top bar", 0, 0, w, 64, "#E9E9E9", "#D0D0D0");
  await createWireText(frame, spec.title, 28, 19, Math.min(520, w - 160), 24, 20);
  await createWireText(frame, `${spec.surface || "panel"} / ${layout}`, w - 230, 22, 174, 22, 12);
  createWireRect(frame, "wireframe close", w - 42, 22, 18, 18, "#D0D0D0", "#AAAAAA");
  const marker = await createWireText(frame, `wireframe layout ${layout}`, 1, 1, 1, 1, 1, "#F8F8F8");
  marker.name = `wireframe layout ${layout}`;

  const bodyTop = 86;
  const bodyHeight = h - bodyTop - 86;
  if (layout === "selection") await drawSelectionLayout(frame, spec, 28, bodyTop, w - 56, bodyHeight);
  else if (layout === "confirmation") await drawConfirmationLayout(frame, spec, 28, bodyTop, w - 56, bodyHeight);
  else if (layout === "successFeedback") await drawResultLayout(frame, spec, 28, bodyTop, w - 56, bodyHeight);
  else if (layout === "systemFeedback") await drawSystemFeedbackLayout(frame, spec, 28, bodyTop, w - 56, bodyHeight);
  else await drawPrimaryOperationLayout(frame, spec, 28, bodyTop, w - 56, bodyHeight);
  if (spec.toast) await drawToast(frame, spec.toast, w);
  if (spec.modal) await drawModal(frame, spec.modal, w, h);
}

function chooseWireframeLayout(spec) {
  const signal = [
    spec.layoutIntent,
    spec.surfaceType,
    spec.surface,
    spec.title,
    spec.primaryTask,
    spec.description,
    ...(spec.regions || []).map((region) => `${region.role || ""} ${region.type || ""} ${region.title || ""}`),
    ...(spec.controls || []).map((control) => `${control.role || ""} ${control.type || ""} ${control.label || ""}`)
  ].join(" ").toLowerCase();
  if (/confirmation|confirm|modal|二次|确认|取消/.test(signal)) return "confirmation";
  if (/successfeedback|result|success|成功|结果|获得|奖励|反馈/.test(signal)) return "successFeedback";
  if (/selection|select|filter|sort|inventory|选择|筛选|排序|背包|数量/.test(signal)) return "selection";
  if (/systemfeedback|system|refresh|reset|mail|toast|后台|系统|刷新|重置|邮件|提醒|红点/.test(signal)) return "systemFeedback";
  return "primaryOperation";
}

